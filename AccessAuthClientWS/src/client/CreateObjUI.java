    private void buttonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOKActionPerformed
        // TODO add your handling code here:

        if(nameObjectCreate.getText().trim().isEmpty())
        {
          JOptionPane.showMessageDialog(this, "Informe um nome v√°lido.", APP_NAME, INFORMATION_MESSAGE, null);
          return;
        }
        setObjName(nameObjectCreate.getText());

        switch (cBoxObject.getSelectedItem().toString()) {
            case "Circle":
                setObject(new Circle());
                doClose(RET_OK);
                break;
            case "Square":
                setObject(new Square());
                doClose(RET_OK);
                break;
            default: doClose(RET_CANCEL);
            JOptionPane.showMessageDialog(this, 
                "Object no created", 
                APP_NAME,INFORMATION_MESSAGE,null);
                break;
        }
    }//GEN-LAST:event_buttonOKActionPerformed

